// This has all the types of operators in JS

let a = 51;
let b = 7;
let c = 50;

console.log("ARITHMETIC OPERATORS");
console.log("a + b:", a + b);
console.log("a - b:", a - b);
console.log("a * b:", a * b);
console.log("a / b:", a / b);
console.log("a ** b:", a ** b);
console.log("a % b:", a % b);
console.log("c++:", c++);
console.log("++c:", ++c);
console.log("c--:", c--);
console.log("--c:", --c);

console.log("ASSIGNMENT OPERATORS");
console.log("a += b:", (a += b));
console.log("a -= b:", (a -= b));
console.log("a *= b:", (a *= b));
console.log("a /= b:", (a /= b));
console.log("a %= b:", (a %= b));
console.log("a **= b:", (a **= b));

console.log("COMPARISION OPERATORS");
console.log("a == b:", a == b);
console.log("a === b:", a === b);
console.log("a != b:", a != b);
console.log("a !== b:", a !== b);
console.log("a < b:", a < b);
console.log("a > b:", a > b);
console.log("a <= b:", a <= b);
console.log("a <= b:", a >= b);

console.log("LOGICAL OPERATORS");
console.log("a && b:", a && b);
console.log("a || b:", a || b);
console.log("!true:", !true);
