// This is Introduction to Variables in JS!
// We can create variables in main 2 ways but only one is recommended!

// Using var or let (We Must use 'let' because its recommended by the ES6 in 2015 due to problems with var because its scope is global and can be called from any where and let is block scope means if written inside a function then cannot be used from outside!)

var myStr = "Hello World!";
let myInt = 54;

console.log(myStr);
console.log(myInt);
