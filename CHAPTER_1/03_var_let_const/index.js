// This has what is let, var, const
// let is modern and best way to create our variables
// var is old and not safe way to create our variables
// const is a constant added in ES6 in 2015 for holding a variable that cannot be changed again after declare we also can use a define() function to create the same constant but it a bit different from this!

var a = "This is var which is global scoped!";
let b = "This is let! which can be changed and blockedscoped!";
const c = "This is const which cannot be changed and blockscoped!";

console.log(a);
console.log(b);
console.log(c);
